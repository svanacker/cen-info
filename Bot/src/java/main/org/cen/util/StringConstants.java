package org.cen.util;

public final class StringConstants {
	/** LF for LCD. */
	public static final char STR_LF = 0x0A;

	/** CLS for LCD. */
	public static final char STR_CLS = 0x0C;

	/** CR for LCD. */
	public static final char STR_CR = 0x0D;

	/** CRLF for LCD. */
	public static final String STR_CRLF = "" + STR_CR + STR_LF;
}
