package org.cen.ui.gameboard;

/**
 * Drawing flags.
 * 
 * @author Emmanuel ZURMELY
 */
public enum GameBoardFlags {

	/**
	 * Flag for drawing the objects.
	 */
	OBJECTS,
	/**
	 * Flag for drawing the obstacles.
	 */
	OBSTACLES,
	/**
	 * Flag for drawing the paths.
	 */
	PATHS, /**
	 * Flag for drawing the trajectory.
	 */
	TRAJECTORY,
	/**
	 * Flag for displaying the labels.
	 */
	LABELS,
	/**
	 * Flag for displaying the path weights.
	 */
	PATHS_WEIGHTS,
	/**
	 * Flag for displaying the opponent.
	 */
	OPPONENT,
	/**
	 * Flag for displaying the targets
	 */
	TARGETS
}
